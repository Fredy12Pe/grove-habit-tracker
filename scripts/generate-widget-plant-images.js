/**
 * Reads static require() paths from lib/game/plantSprites.ts (28×7 order)
 * and writes targets/widget/plantImages.generated.js for @bacons/apple-targets `images`.
 */
const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");
const plantSpritesPath = path.join(root, "lib", "game", "plantSprites.ts");
const outPath = path.join(root, "targets", "widget", "plantImages.generated.js");

const src = fs.readFileSync(plantSpritesPath, "utf8");
const re = /require\(\s*["']@\/([^"']+)["']\s*\)/g;
const paths = [];
let m;
while ((m = re.exec(src)) !== null) {
  paths.push(m[1].replace(/\\/g, "/"));
}

const FRAMES = 7;
const PLANTS = 28;
const expected = PLANTS * FRAMES;
if (paths.length !== expected) {
  throw new Error(
    `Expected ${expected} plant require() paths in plantSprites.ts, found ${paths.length}`
  );
}

const images = {};
for (let i = 0; i < paths.length; i++) {
  const plantIndex = Math.floor(i / FRAMES);
  const frameIndex = i % FRAMES;
  const key = `plant_${plantIndex}_${frameIndex}`;
  images[key] = `../../${paths[i]}`;
}

const header =
  "/* eslint-disable */\n/** Auto-generated by scripts/generate-widget-plant-images.js — do not edit. */\nmodule.exports = ";
fs.writeFileSync(outPath, header + JSON.stringify(images, null, 2) + ";\n");
